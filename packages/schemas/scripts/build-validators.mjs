#!/usr/bin/env node
/*
 * Codegen for @soa-harness/schemas.
 *
 * Pipeline:
 *   1. Read soa-validate.lock to discover the pinned spec commit.
 *   2. Verify the sibling ../soa-harness=specification/ repo is checked out at that commit.
 *   3. Copy schemas/*.schema.json into src/schemas/vendored/ (gitignored).
 *   4. Compile each with Ajv 2020-12 + ajv-formats into a standalone ES module (src/validators/generated/*.js + .d.ts).
 *   5. Generate src/registry.ts re-exporting every validator under a typed registry.
 *
 * Fails loudly if the sibling is missing, on the wrong commit, or if any schema fails to compile.
 */

import { readFileSync, writeFileSync, mkdirSync, readdirSync, rmSync, cpSync, existsSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import { execSync } from "node:child_process";
import Ajv2020 from "ajv/dist/2020.js";
import addFormats from "ajv-formats";
import standaloneCode from "ajv/dist/standalone/index.js";

const here = dirname(fileURLToPath(import.meta.url));
const pkgRoot = join(here, "..");
const repoRoot = join(pkgRoot, "..", "..");
const specRoot = join(repoRoot, "..", "soa-harness=specification");

function die(msg) {
  console.error(`\n[schemas/build-validators] ${msg}\n`);
  process.exit(1);
}

// 1. Read pin
const lockPath = join(repoRoot, "soa-validate.lock");
if (!existsSync(lockPath)) die(`soa-validate.lock not found at ${lockPath}`);
const lock = JSON.parse(readFileSync(lockPath, "utf8"));
const pinnedSha = lock.spec_commit_sha;
if (!pinnedSha) die("soa-validate.lock has no spec_commit_sha");

// 2. Verify sibling at pin
if (!existsSync(specRoot)) die(`Sibling spec repo not found at ${specRoot}`);
let actualSha;
try {
  actualSha = execSync("git rev-parse HEAD", { cwd: specRoot, encoding: "utf8" }).trim();
} catch (err) {
  die(`Could not read HEAD of sibling spec repo at ${specRoot}: ${err.message}`);
}
if (actualSha !== pinnedSha) {
  die(
    `Sibling spec repo is at ${actualSha.slice(0, 12)} but soa-validate.lock pins ${pinnedSha.slice(0, 12)}.\n` +
      `Run: (cd ${specRoot} && git fetch && git checkout ${pinnedSha})\n` +
      `Or bump the pin via the protocol in soa-validate.lock.pin_bump_protocol.`
  );
}

// 3. Copy schemas
const srcSchemasDir = join(specRoot, "schemas");
if (!existsSync(srcSchemasDir)) die(`Spec has no schemas/ directory at ${srcSchemasDir}`);
const vendoredDir = join(pkgRoot, "src", "schemas", "vendored");
rmSync(vendoredDir, { recursive: true, force: true });
mkdirSync(vendoredDir, { recursive: true });
const schemaFiles = readdirSync(srcSchemasDir).filter((f) => f.endsWith(".schema.json"));
if (schemaFiles.length === 0) die(`No *.schema.json files in ${srcSchemasDir}`);
for (const f of schemaFiles) {
  cpSync(join(srcSchemasDir, f), join(vendoredDir, f));
}
writeFileSync(join(vendoredDir, "PINNED_COMMIT.txt"), `${pinnedSha}\n`);

// 4. Compile validators
const Ajv = Ajv2020.default ?? Ajv2020;
const addFormatsFn = addFormats.default ?? addFormats;
const standalone = standaloneCode.default ?? standaloneCode;

const ajv = new Ajv({
  strict: false,
  allErrors: false,
  allowUnionTypes: true,
  code: { source: true, esm: true }
});
addFormatsFn(ajv);

const loaded = [];
for (const f of schemaFiles) {
  const schema = JSON.parse(readFileSync(join(vendoredDir, f), "utf8"));
  const id = schema.$id ?? f;
  ajv.addSchema(schema, id);
  const name = f.replace(/\.schema\.json$/, "");
  loaded.push({ name, id, file: f });
}

const generatedDir = join(pkgRoot, "src", "validators", "generated");
rmSync(generatedDir, { recursive: true, force: true });
mkdirSync(generatedDir, { recursive: true });

for (const { name, id } of loaded) {
  const validate = ajv.getSchema(id);
  if (!validate) die(`Ajv could not resolve schema $id=${id} for ${name}`);
  const jsSource = standalone(ajv, validate);
  writeFileSync(join(generatedDir, `${name}.js`), jsSource);
  writeFileSync(
    join(generatedDir, `${name}.d.ts`),
    `import type { ValidateFunction } from "ajv";\ndeclare const validate: ValidateFunction<unknown>;\nexport default validate;\n`
  );
}

// 5. Emit registry.ts
function toCamel(name) {
  return name.replace(/-([a-z0-9])/g, (_, c) => c.toUpperCase());
}
const importLines = loaded.map(({ name }) => `import ${toCamel(name)} from "./validators/generated/${name}.js";`);
const entryLines = loaded.map(({ name }) => `  "${name}": ${toCamel(name)} as Validator,`);
const registry = [
  "// AUTO-GENERATED by scripts/build-validators.mjs. Do not edit.",
  `// Pinned spec commit: ${pinnedSha}`,
  'import type { ValidateFunction } from "ajv";',
  ...importLines,
  "",
  "export type Validator = ValidateFunction<unknown>;",
  "",
  "export const registry = {",
  ...entryLines,
  "} as const;",
  "",
  "export type SchemaName = keyof typeof registry;",
  "export const schemaNames = Object.keys(registry) as SchemaName[];",
  ""
].join("\n");
writeFileSync(join(pkgRoot, "src", "registry.ts"), registry);

console.log(`[schemas] generated ${loaded.length} validators from spec ${pinnedSha.slice(0, 12)}`);
